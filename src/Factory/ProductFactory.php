final class ProductFactory extends ModelFactory
{
    /**
     * @see https://symfony.com/bundles/ZenstruckFoundryBundle/current/index.html#factories-as-services
     *
     * @todo inject services if required
     */
    public function __construct()
    {
        parent::__construct();
    }

    /**
     * @see https://symfony.com/bundles/ZenstruckFoundryBundle/current/index.html#model-factories
     *
     * @todo add your default values here
     */
    protected function getDefaults(): array
    {
        return [
            'name' => self::faker()->text(180),
            'description' => self::faker()->text(180),
            'price' => self::faker()->numberBetween(1_00, 1000_00),
            'sku' => self::faker()->uuid,
            'taxCategory' => LazyValue::new(fn () => TaxCategoryFactory::createOne()),
        ];
    }

    /**
     * @see https://symfony.com/bundles/ZenstruckFoundryBundle/current/index.html#initialization
     */
    protected function initialize(): self
    {
        return $this
            // ->afterInstantiate(function(User $user): void {})
        ;
    }

    public function published(): self
    {
        return $this->addState(['publishedAt' => self::faker()->dateTimeBetween('-1 year', 'now')]);
    }

    public function unpublished(): self
    {
        return $this->addState(['publishedAt' => null]);
    }

    protected static function getClass(): string
    {
        return Product::class;
    }
}
